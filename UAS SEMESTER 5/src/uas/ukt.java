    public void tampilDataBooking(){
        try {
            modelTabelBooking.getDataVector().removeAllElements();
            modelTabelBooking.fireTableDataChanged();
        
            PreparedStatement query = myObject.Konek.prepareStatement("SELECT * FROM ukt");
            ResultSet data = query.executeQuery();
            while (data.next()) {
            Object[] baris = new Object[4];
            baris[0] = data.getString("no_ukt");
            baris[1] = data.getString("npm");
            baris[2] = data.getString("nama_mahasiswa");
            baris[3] = data.getString("keterangan");
            modelTabelBooking.addRow(baris);
            
            }
            query.close();
            data.close();
            } catch (Exception e){
            }
        
    
    }


