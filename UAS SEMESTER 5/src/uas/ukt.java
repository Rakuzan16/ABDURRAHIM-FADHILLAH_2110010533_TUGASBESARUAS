        // Menggunakan PreparedStatement untuk mencegah SQL Injection
        String query = "UPDATE ukt SET npm = ?, nama_mahasiswa = ?, keterangan = ? WHERE no_ukt = ?";
        PreparedStatement preparedStatement = connection.prepareStatement(query);

        // Set nilai parameter PreparedStatement
        preparedStatement.setString(1, jTextField2.getText()); // Ambil nilai dari JTextField2 (npm)
        preparedStatement.setString(2, jTextField3.getText()); // Ambil nilai dari JTextField3 (nama_mahasiswa)

        // Ambil nilai checkbox yang dipilih dan sesuaikan dengan kolom 'keterangan'
        StringBuilder keterangan = new StringBuilder();
        if (jCheckBox1.isSelected()) {
            keterangan.append("Rp.1.500.000");
        }
        if (jCheckBox2.isSelected()) {
            if (keterangan.length() > 0) {
                keterangan.append(", ");
            }
            keterangan.append("Rp.867.000");
        }
        if (jCheckBox3.isSelected()) {
            if (keterangan.length() > 0) {
                keterangan.append(", ");
            }
            keterangan.append("Rp.867.000");
        }
        if (jCheckBox4.isSelected()) {
            if (keterangan.length() > 0) {
                keterangan.append(", ");
            }
            keterangan.append("Lunas");
        }

        preparedStatement.setString(3, keterangan.toString()); // Set nilai kolom 'keterangan'
        preparedStatement.setString(4, jTextField1.getText()); // Ambil nilai dari JTextField1 (no_ukt)

