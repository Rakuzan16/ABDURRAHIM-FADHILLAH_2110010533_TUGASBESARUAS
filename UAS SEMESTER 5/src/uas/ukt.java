    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       // Logika penyimpanan data
        // Konfigurasi koneksi database
        String url = "jdbc:mysql://localhost:3306/krs";
        String username = "root";
        String password = "";

        try {
            // Membuat koneksi
            Connection connection = DriverManager.getConnection(url, username, password);

            // Inisialisasi nilai default
            String harga1 = "Rp.1.500.000";
            String harga2 = "Rp.867.000";
            String harga3 = "Rp.867.000";
            String lunas = "Lunas";

            // Menggunakan PreparedStatement untuk mencegah SQL Injection
            String query = "INSERT INTO ukt (no_ukt, npm, nama_mahasiswa, keterangan) VALUES (?, ?, ?, ?)";
            PreparedStatement preparedStatement = connection.prepareStatement(query);

            // Set nilai parameter PreparedStatement
            preparedStatement.setString(1, jTextField1.getText()); // Ambil nilai dari JTextField1 (no_ukt)
            preparedStatement.setString(2, jTextField2.getText()); // Ambil nilai dari JTextField2 (npm)
            preparedStatement.setString(3, jTextField3.getText()); // Ambil nilai dari JTextField3 (nama_mahasiswa)

            // Ambil nilai checkbox yang dipilih dan sesuaikan dengan kolom 'keterangan'
            StringBuilder keterangan = new StringBuilder();
            if (jCheckBox1.isSelected()) {
                keterangan.append(harga1);
            }
            if (jCheckBox2.isSelected()) {
                if (keterangan.length() > 0) {
                    keterangan.append(", ");
                }
                keterangan.append(harga2);
            }
            if (jCheckBox3.isSelected()) {
                if (keterangan.length() > 0) {
                    keterangan.append(", ");
                }
                keterangan.append(harga3);
            }
            if (jCheckBox4.isSelected()) {
                if (keterangan.length() > 0) {
                    keterangan.append(", ");
                }
                keterangan.append(lunas);
            }

            preparedStatement.setString(4, keterangan.toString()); // Set nilai kolom 'keterangan'

            // Eksekusi query
            preparedStatement.executeUpdate();

            System.out.println("Data berhasil disimpan ke tabel ukt di database krs.");

            // Tutup koneksi
            preparedStatement.close();
            connection.close();

        } catch (SQLException e) {
            e.printStackTrace();
               
        }
        tampilDataBooking();
      
    }//GEN-LAST:event_jButton1ActionPerformed

